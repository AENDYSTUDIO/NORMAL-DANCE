export type ATRTranslationKey = keyof typeof ATR_TRANSLATIONS.common['zh-CN'];

export const getTranslation = (
  language: string,
  category: keyof typeof ATR_TRANSLATIONS,
  key: string
): string => {
  const translations = ATR_TRANSLATIONS[category];
  const langTranslations = translations[language as keyof typeof translations];
  
  if (!langTranslations) {
    // Fallback to English if language not found
    const englishTranslations = translations['en-AU' as keyof typeof translations];
    return englishTranslations?.[key as keyof typeof englishTranslations] || key;
  }
  
  return langTranslations[key as keyof typeof langTranslations] || key;
};
